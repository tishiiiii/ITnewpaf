package com.example.cookingsystem.models;

import org.springframework.data.annotation.Id;
import org.springframework.data.mongodb.core.mapping.Document;

@Document(collection = "tasks")
public class Task {
    @Id
    private String id;
    private String title;
    private String description;
    private String type; // beginner, intermediate, advanced
    private int estimateTime; // in minutes
    private String imageUrl;
    private boolean deleteStatus;

    // Default constructor
    public Task() {
    }

    // Overloaded constructor
    public Task(String id, String title, String description, String type, int estimateTime, String imageUrl, boolean deleteStatus) {
        this.id = id;
        this.title = title;
        this.description = description;
        this.type = type;
        this.estimateTime = estimateTime;
        this.imageUrl = imageUrl;
        this.deleteStatus = deleteStatus;
    }

    // Getters and setters

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getEstimateTime() {
        return estimateTime;
    }

    public void setEstimateTime(int estimateTime) {
        this.estimateTime = estimateTime;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public boolean isDeleteStatus() {
        return deleteStatus;
    }

    public void setDeleteStatus(boolean deleteStatus) {
        this.deleteStatus = deleteStatus;
    }
}
